CREATE TABLE Code.Usuario(Id_Usuarios INT AUTO_INCREMENT NOT NULL, 
Nombre VARCHAR(255) NOT NULL,
ApellidoP VARCHAR(255) NOT NULL,
ApellidoM VARCHAR(255) NOT NULL,
Id_Tusuario INT NOT NULL,
Telefono VARCHAR(255) NOT NULL,
Email VARCHAR(255) NOT NULL,
UserName VARCHAR(255) NOT NULL,
Password VARCHAR(255) NOT NULL,
Online INT NOT NULL,
Id_Estatus INT NOT NULL,
ImgUser VARCHAR(255) NOT NULL,
Id_Plantel INT NOT NULL,
Id_Expediente INT NOT NULL, PRIMARY KEY (Id_Usuarios))ENGINE=InnoDB;